package com.peaksoft.Classes;

public class ThePresident {
    private int id;
    private String first_name;
    private String last_name;
    private String year_of_birth;
    private String woman_name;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getFirst_name() {
        return first_name;
    }

    public void setFirst_name(String first_name) {
        this.first_name = first_name;
    }

    public String getLast_name() {
        return last_name;
    }

    public void setLast_name(String last_name) {
        this.last_name = last_name;
    }

    public String getYear_of_birth() {
        return year_of_birth;
    }

    public void setYear_of_birth(String year_of_birth) {
        this.year_of_birth = year_of_birth;
    }

    public String getWoman_name() {
        return woman_name;
    }

    public void setWoman_name(String woman_name) {
        this.woman_name = woman_name;
    }
}
